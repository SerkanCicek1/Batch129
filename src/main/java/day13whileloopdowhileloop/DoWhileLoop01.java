package day13whileloopdowhileloop;

public class DoWhileLoop01 {

    public static void main(String[] args) {

        // 1) while-loop           While--> once sartı sagladıgını kontrol eder sonra calısır
        int i = 1;

        while (i < 1){
            System.out.println("Sen bir while loopsun...");
            i++;
        }

        // 2) do-while-loop        DoWhile--> once calısır sonra sartı kontrol eder
        int k = 1;

        do{
            System.out.println("Sen bir do while loopsun...");
            k++;
        }while(k < 1);





    }

}
